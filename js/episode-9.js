// Declaration et récupération de la variable
let moyenne = prompt("saisissez votre moyenne:");
// Traitement des conditions et affichage
if(moyenne>=0 && moyenne<10){
        alert("Votre mention est: Passable");
     }
     else if(moyenne>10 && moyenne<=14){
        alert("Votre mention est: Assez-bien");
     }
     else if(moyenne>14 && moyenne<=16){
        alert("Votre mention est: Bien");
     }
     else if(moyenne>16 && moyenne<=20){
        alert("Votre mention est: Excellent");
     }
     else {
        alert("Erreur");
     }


     
